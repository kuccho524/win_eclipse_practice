package jp.win.JavaPractice;

public class Variable01 {
	public static void main(String[] args) {
		int num1;
		String str1;

		// num1に10, str1に「Hello World!」を代入
		num1 = 10;
		str1 = "Hello World!";
		System.out.println(num1);
		System.out.println(str1);

		// num1に20を代入
		num1 = 20;
		System.out.println(num1);

		// int型を持つ変数num2に30を代入
		int num2 = 30;
		System.out.println(num2);
	}
}
